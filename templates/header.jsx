export function renderHeader() {
  const contactList = Object.entries(contacts)
    .filter(([key]) => icons[key])
    .map(([key, value]) => {
      const { url, svg } = icons[key];
      return (
        <li>
          <a
            href={url(value)}
            className={`contact-icon contact-${key}`}
            title={key}
            target="_blank"
            rel="noopener noreferrer"
            dangerouslySetInnerHTML={{ __html: svg }}
          />
        </li>
      );
    });

  return (
    <header className="site-header">
      <a href="/">
        <h1>{siteTitle}</h1>
        <h2>{siteAuthor}</h2>
      </a>
      <ul className="contact-list">{contactList}</ul>
    </header>
  );
}
